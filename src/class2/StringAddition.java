package class2;

public class StringAddition {
    public static void main(String[] args) {
        String firstName="George";
        String lastName="Michael";
        System.out.println(firstName+" "+lastName);
        System.out.println("First name = " + firstName); //this is how it looks if you want to add a space between name, = and george
        System.out.println("10"+"10"); // through sout operation we can only COMBINE two different operations because Java only knows how to do that.
        // every other operation, Java will not recognize it.
    }
}
