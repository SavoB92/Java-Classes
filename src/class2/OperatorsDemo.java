package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1*number2);
        System.out.println(number1/number2); //when we divide, if the result would be anythig with decimal such as 0.5 in this case, Java reads it as 0.
       /*
       1)if you have to store whole numbers always use INT
       2)if you have to store decimal numbers always use double
       3)if you have to store characters you can always use String
        */
    }
}
