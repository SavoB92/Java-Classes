package class4;

public class IfElseConditions2 {
    public static void main(String[] args) {

        boolean rain=true;

        if(rain){
            System.out.println("Lets take the umbrella!"); //this is how we set up the if else conditions, if it is true the line code #9 is being used, if not, line code #11 is being used.
        }else{
            System.out.println("Lets just take the hat!");
        }
        boolean breakTime=true;

        if(breakTime){
            System.out.println("Lets take a break!");
        }else{
            System.out.println("Lets continue the class!");
        }
    }
}
