package class7;
public class ForLoops3 {
    /*
    When you exactly know how many times you want the loop the block of code,  we use for loop.
     */
    public static void main(String[] args) {
        for (int i=0; i < 10; i++) {
            System.out.println(i);
        }
        /*
        int i=0;
        i < 10;
        System.out.println(i);
        i++
        i < 10 (again, condition is checked)
        System.out.println(i);
         */
    }
}
