package class6;

public class IncrementOperator {
    public static void main(String[] args) {

        int number=10;
        number=number+1; // 11 in value.
        System.out.println(number);
        number+=1; // 12 in value.
        System.out.println(number);
        number++; // 13 in value.
        System.out.println(number);

        int number2=10;
        number2--; // 9 in value.
        System.out.println(number2);
    }
}
